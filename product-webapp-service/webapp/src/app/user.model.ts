export class User {
    
    firstName: string;
    lastName: string;
    email: string;
    phone: string;
    
    age: string;
    address: string;
    height: string;
    
    gender: string;
   
    weight: string;
    
  
    constructor(
      
      firstName: string,
      lastName: string,
      email: string,
      phone: string,
      
      age: string,
      address: string,
      height: string,
      
      gender: string,
      
      weight: string,
      
    ) {
      
      this.firstName = firstName;
      this.lastName = lastName;
      this.email = email;
      this.phone = phone;
      
      this.age = age;
      this.address = address;
      this.height = height;
      
      this.gender = gender;
      
      this.weight = weight;
     
    }
  }
  